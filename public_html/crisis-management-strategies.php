                </ul>

                <h4 class="text-xl font-semibold mt-6 mb-3">Employees</h4>
                <ul class="list-disc pl-6 mb-6">
                    <li>Provide internal communication before external</li>
                    <li>Address job security and company stability concerns</li>
                    <li>Give employees tools to respond to external questions</li>
                    <li>Maintain regular updates throughout the crisis</li>
                </ul>

                <h4 class="text-xl font-semibold mt-6 mb-3">Investors</h4>
                <ul class="list-disc pl-6 mb-6">
                    <li>Focus on financial impact and business continuity</li>
                    <li>Provide detailed analysis and projections</li>
                    <li>Emphasize management competence and control</li>
                    <li>Follow all regulatory disclosure requirements</li>
                </ul>

                <h4 class="text-xl font-semibold mt-6 mb-3">Media</h4>
                <ul class="list-disc pl-6 mb-6">
                    <li>Provide timely, accurate information</li>
                    <li>Make spokespersons available for interviews</li>
                    <li>Offer exclusive access or insights when appropriate</li>
                    <li>Build relationships for ongoing coverage</li>
