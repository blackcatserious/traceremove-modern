                    <li><strong>Denial or Cover-up:</strong> Attempting to hide or deny legitimate issues</li>
                    <li><strong>Blame-shifting:</strong> Pointing fingers at others instead of taking responsibility</li>
                    <li><strong>Inconsistent Messaging:</strong> Different spokespersons providing conflicting information</li>
                    <li><strong>Ignoring Stakeholders:</strong> Failing to address concerns of key stakeholder groups</li>
                    <li><strong>Over-promising:</strong> Making commitments that cannot be kept</li>
                    <li><strong>Inadequate Follow-up:</strong> Failing to provide updates and progress reports</li>
                    <li><strong>Learning Nothing:</strong> Not using the crisis as an opportunity for improvement</li>
                </ul>

