                                    </div>
                                </div>
                                <div>
                                    <div class="mb-4">
                                        <strong class="text-gray-900">NOT Operator:</strong>
                                        <code class="block bg-white p-2 mt-1 rounded text-sm">"reputation" NOT "celebrity"</code>
                                        <p class="text-gray-600 text-sm mt-1">Exclude unwanted terms</p>
                                    </div>
                                    <div class="mb-4">
                                        <strong class="text-gray-900">Proximity Search:</strong>
                                        <code class="block bg-white p-2 mt-1 rounded text-sm">"reputation" NEAR/5 "management"</code>
