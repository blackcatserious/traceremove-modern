                        <h3 class="text-2xl font-semibold mb-4">The SOAR Framework</h3>
                        <p class="text-gray-600 mb-6">
                            Use the SOAR framework to structure your crisis communications effectively:
                        </p>
                        
                        <div class="space-y-6 mb-8">
                            <div class="border-l-4 border-blue-500 pl-6">
                                <h4 class="text-xl font-semibold mb-3 text-blue-900">S - Situation</h4>
                                <p class="text-gray-600 mb-3">Clearly state what happened without speculation or blame. Stick to verified facts and avoid emotional language.</p>
                                <div class="bg-blue-50 p-4 rounded-lg">
                                    <strong>Example:</strong> "We have identified a technical issue that affected our customer database between [time] and [time] on [date]."
                                </div>
                            </div>
                            
                            <div class="border-l-4 border-purple-500 pl-6">
                                <h4 class="text-xl font-semibold mb-3 text-purple-900">O - Ownership</h4>
                                <p class="text-gray-600 mb-3">Take responsibility where appropriate. Acknowledge the impact on stakeholders and express genuine concern.</p>
                                <div class="bg-purple-50 p-4 rounded-lg">
                                    <strong>Example:</strong> "We take full responsibility for this incident and sincerely apologize to all affected customers."
                                </div>
                            </div>
                            
                            <div class="border-l-4 border-green-500 pl-6">
                                <h4 class="text-xl font-semibold mb-3 text-green-900">A - Action</h4>
