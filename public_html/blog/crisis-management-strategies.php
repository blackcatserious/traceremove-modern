                                <div class="bg-green-50 p-4 rounded-lg">
                                    <strong>Example:</strong> "We have immediately secured the affected systems and are working with cybersecurity experts to conduct a thorough investigation."
                                </div>
                            </div>
                            
                            <div class="border-l-4 border-red-500 pl-6">
                                <h4 class="text-xl font-semibold mb-3 text-red-900">R - Resolution</h4>
                                <p class="text-gray-600 mb-3">Provide a timeline for resolution and ongoing updates. Offer specific next steps and contact information.</p>
                                <div class="bg-red-50 p-4 rounded-lg">
                                    <strong>Example:</strong> "We will provide updates every 4 hours until this issue is fully resolved. Affected customers can contact us at [contact info]."
