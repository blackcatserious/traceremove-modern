                if (entry.isIntersecting) {
                    const element = entry.target;
                    
                    element.classList.add('fade-in-up');
                    
                    contentObserver.unobserve(element);
                }
            });
        }, observerOptions);
        
        document.querySelectorAll('section, .card, .service-item, .testimonial, .pricing-card').forEach(el => {
            contentObserver.observe(el);
        });
        
        const preloadResources = [
            '/assets/css/traceremove_main.css',
            '/assets/images/logo.svg',
            '/assets/images/logo-white.svg'
        ];
        
        preloadResources.forEach(resource => {
            const link = document.createElement('link');
            link.rel = 'preload';
            link.as = resource.endsWith('.css') ? 'style' : 'image';
            link.href = resource;
            document.head.appendChild(link);
        });
        
        let loadingProgress = 0;
        const progressIndicator = document.createElement('div');
        progressIndicator.style.cssText = `
            position: fixed;
            bottom: 20px;
            right: 20px;
            background: rgba(102, 126, 234, 0.9);
            color: white;
            padding: 10px 15px;
            border-radius: 20px;
            font-size: 12px;
            z-index: 99997;
            opacity: 0;
            pointer-events: none;
        `;
        progressIndicator.textContent = 'Loading...';
        document.body.appendChild(progressIndicator);
        
        const progressTimeout = setTimeout(() => {
            gsap.to(progressIndicator, {
                opacity: 1,
                duration: 0.3,
                ease: "power2.out"
            });
        }, 1000);
        
        window.addEventListener('load', () => {
            clearTimeout(progressTimeout);
            gsap.to(progressIndicator, {
                opacity: 0,
                duration: 0.3,
                ease: "power2.out",
                onComplete: () => progressIndicator.remove()
            });
        });
        
        
        const isMobile = /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent) || window.innerWidth <= 768;
        
        if (isMobile) {
            document.querySelectorAll('.btn-premium, .btn-premium-glow, .btn-premium-outline, button, .card, .service-item').forEach(element => {
                element.addEventListener('touchstart', (e) => {
                    gsap.to(element, {
                        scale: 0.95,
                        duration: 0.1,
                        ease: "power2.out"
                    });
                    
                    const touch = e.touches[0];
                    const rect = element.getBoundingClientRect();
                    const ripple = document.createElement('div');
                    
                    ripple.style.cssText = `
                        position: absolute;
                        border-radius: 50%;
                        background: rgba(102, 126, 234, 0.3);
                        transform: scale(0);
                        pointer-events: none;
                        left: ${touch.clientX - rect.left}px;
                        top: ${touch.clientY - rect.top}px;
                        width: 20px;
                        height: 20px;
                        margin-left: -10px;
                        margin-top: -10px;
                    `;
                    
                    element.style.position = 'relative';
                    element.style.overflow = 'hidden';
                    element.appendChild(ripple);
                    
                    gsap.to(ripple, {
                        scale: 8,
                        opacity: 0,
                        duration: 0.6,
                        ease: "power2.out",
                        onComplete: () => ripple.remove()
                    });
                });
                
                element.addEventListener('touchend', () => {
                    gsap.to(element, {
                        scale: 1,
                        duration: 0.2,
                        ease: "back.out(1.7)"
                    });
                });
                
                element.addEventListener('touchcancel', () => {
                    gsap.to(element, {
                        scale: 1,
                        duration: 0.2,
                        ease: "power2.out"
                    });
                });
            });
            
            gsap.utils.toArray('.fade-in, .scale-in, .slide-in-left, .slide-in-right').forEach(element => {
                gsap.set(element, {
                    willChange: 'transform, opacity'
                });
                
                gsap.fromTo(element, 
                    { opacity: 0, y: 20 },
                    {
                        opacity: 1,
                        y: 0,
                        duration: 0.4,
                        ease: "power2.out",
                        scrollTrigger: {
                            trigger: element,
                            start: "top 90%",
                            end: "bottom 10%",
                            toggleActions: "play none none none"
                        }
                    }
                );
            });
            
            const mobileMenuToggle = document.querySelector('.mobile-menu-toggle');
            const mobileMenu = document.querySelector('.mobile-menu');
            
            if (mobileMenuToggle && mobileMenu) {
                mobileMenuToggle.addEventListener('touchstart', () => {
                    gsap.to(mobileMenuToggle, {
                        scale: 0.9,
                        duration: 0.1,
                        ease: "power2.out"
                    });
                });
                
                mobileMenuToggle.addEventListener('touchend', () => {
                    gsap.to(mobileMenuToggle, {
                        scale: 1,
                        duration: 0.2,
                        ease: "back.out(1.7)"
                    });
                });
                
                const menuItems = mobileMenu.querySelectorAll('a');
                mobileMenuToggle.addEventListener('click', () => {
                    if (mobileMenu.classList.contains('hidden')) {
                        mobileMenu.classList.remove('hidden');
                        gsap.fromTo(mobileMenu, 
                            { x: '100%', opacity: 0 },
                            { x: '0%', opacity: 1, duration: 0.3, ease: "power2.out" }
                        );
                        
                        gsap.fromTo(menuItems, 
                            { x: 50, opacity: 0 },
                            { 
                                x: 0, 
                                opacity: 1, 
                                duration: 0.2, 
                                stagger: 0.05,
                                delay: 0.1,
                                ease: "power2.out" 
                            }
                        );
                    } else {
                        gsap.to(mobileMenu, {
                            x: '100%',
                            opacity: 0,
                            duration: 0.3,
                            ease: "power2.in",
                            onComplete: () => mobileMenu.classList.add('hidden')
                        });
                    }
                });
            }
            
            const testimonialSlider = document.querySelector('.testimonials-slider');
            if (testimonialSlider) {
                let startX = 0;
                let currentX = 0;
                let isDragging = false;
                
                testimonialSlider.addEventListener('touchstart', (e) => {
                    startX = e.touches[0].clientX;
                    isDragging = true;
                    gsap.killTweensOf(testimonialSlider);
                });
                
                testimonialSlider.addEventListener('touchmove', (e) => {
                    if (!isDragging) return;
                    
                    currentX = e.touches[0].clientX;
                    const deltaX = currentX - startX;
                    
                    gsap.set(testimonialSlider, {
                        x: deltaX * 0.3
                    });
                });
                
                testimonialSlider.addEventListener('touchend', () => {
                    if (!isDragging) return;
                    isDragging = false;
                    
                    const deltaX = currentX - startX;
                    
                    if (Math.abs(deltaX) > 50) {
                        const direction = deltaX > 0 ? 'prev' : 'next';
                        const event = new CustomEvent('testimonialSwipe', { detail: { direction } });
                        testimonialSlider.dispatchEvent(event);
                    }
                    
                    gsap.to(testimonialSlider, {
                        x: 0,
                        duration: 0.3,
                        ease: "power2.out"
                    });
                });
            }
            
            document.querySelectorAll('input, textarea, select').forEach(input => {
                input.addEventListener('focus', () => {
                    gsap.to(input, {
                        scale: 1.02,
                        duration: 0.2,
                        ease: "power2.out"
                    });
                    
                    setTimeout(() => {
                        input.scrollIntoView({ 
                            behavior: 'smooth', 
                            block: 'center' 
                        });
                    }, 300);
                });
                
                input.addEventListener('blur', () => {
                    gsap.to(input, {
                        scale: 1,
                        duration: 0.2,
                        ease: "power2.out"
                    });
                });
            });
            
            document.addEventListener('DOMContentLoaded', () => {
                if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
                    gsap.globalTimeline.timeScale(0.5);
                }
                
                gsap.set('*', {
                    force3D: true
                });
            });
            
            const scrollIndicators = document.querySelectorAll('.scroll-indicator');
            scrollIndicators.forEach(indicator => {
                indicator.addEventListener('touchstart', () => {
                    gsap.to(indicator, {
                        scale: 1.2,
                        duration: 0.1,
                        ease: "power2.out"
                    });
                });
                
                indicator.addEventListener('touchend', () => {
                    gsap.to(indicator, {
                        scale: 1,
                        duration: 0.2,
                        ease: "back.out(1.7)"
                    });
                });
            });
            
        } else {
            document.querySelectorAll('.card, .service-item, .pricing-card').forEach(card => {
                card.addEventListener('mouseenter', () => {
                    gsap.to(card, {
                        y: -10,
                        scale: 1.02,
                        duration: 0.3,
                        ease: "power2.out"
                    });
                });
                
                card.addEventListener('mouseleave', () => {
                    gsap.to(card, {
                        y: 0,
                        scale: 1,
                        duration: 0.3,
                        ease: "power2.out"
                    });
                });
            });
        }
        
        window.addEventListener('resize', () => {
            ScrollTrigger.refresh();
            
            const newIsMobile = window.innerWidth <= 768;
            if (newIsMobile !== isMobile) {
                location.reload();
            }
        });
        
        window.addEventListener('orientationchange', () => {
            setTimeout(() => {
                ScrollTrigger.refresh();
            }, 500);
        });
        
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            });
        });
        
        const newsletterForm = document.querySelector('.newsletter-form');
        if (newsletterForm) {
            newsletterForm.addEventListener('submit', function(e) {
                e.preventDefault();
                const email = this.querySelector('input[type="email"]').value;
                
                if (!email || !email.includes('@')) {
                    alert('Please enter a valid email address.');
                    return;
                }
                
                alert('Thank you for subscribing! We\'ll keep you updated with the latest insights.');
                this.reset();
            });
        }
        
        const contactForm = document.querySelector('.contact-form');
        if (contactForm) {
            contactForm.addEventListener('submit', function(e) {
                const submitBtn = document.getElementById('submit-btn');
                const submitText = document.getElementById('submit-text');
                const submitLoading = document.getElementById('submit-loading');
                
                const requiredFields = this.querySelectorAll('[required]');
                let isValid = true;
                
                requiredFields.forEach(field => {
                    if (!field.value.trim()) {
                        isValid = false;
                        field.classList.add('border-red-500');
                        field.focus();
                    } else {
                        field.classList.remove('border-red-500');
                    }
                });
                
                const emailField = this.querySelector('input[type="email"]');
                if (emailField && emailField.value && !emailField.value.includes('@')) {
                    isValid = false;
                    emailField.classList.add('border-red-500');
                    emailField.focus();
                }
                
                if (!isValid) {
                    e.preventDefault();
                    
                    const errorDiv = document.createElement('div');
                    errorDiv.className = 'bg-red-50 border border-red-200 rounded-lg p-4 mb-6 text-red-700';
                    errorDiv.innerHTML = '<strong>Please correct the highlighted fields before submitting.</strong>';
                    
                    const existingError = this.querySelector('.bg-red-50');
                    if (existingError) {
                        existingError.remove();
                    }
                    
                    this.insertBefore(errorDiv, this.firstChild);
                    
                    setTimeout(() => {
                        if (errorDiv.parentNode) {
                            errorDiv.remove();
                        }
                    }, 5000);
                    
                    return;
                }
                
                if (submitBtn && submitText && submitLoading) {
                    submitBtn.disabled = true;
                    submitText.classList.add('hidden');
                    submitLoading.classList.remove('hidden');
                }
                
            });
            
            const requiredFields = contactForm.querySelectorAll('[required]');
            requiredFields.forEach(field => {
                field.addEventListener('blur', function() {
                    if (this.value.trim()) {
                        this.classList.remove('border-red-500');
                        this.classList.add('border-green-500');
                    } else {
                        this.classList.add('border-red-500');
                        this.classList.remove('border-green-500');
                    }
                });
                
                field.addEventListener('input', function() {
                    if (this.value.trim()) {
                        this.classList.remove('border-red-500');
                    }
                });
            });
            
            const emailField = contactForm.querySelector('input[type="email"]');
            if (emailField) {
                emailField.addEventListener('blur', function() {
                    if (this.value && this.value.includes('@') && this.value.includes('.')) {
                        this.classList.remove('border-red-500');
                        this.classList.add('border-green-500');
                    } else if (this.value) {
                        this.classList.add('border-red-500');
                        this.classList.remove('border-green-500');
                    }
                });
            }
        }
    </script>
    
